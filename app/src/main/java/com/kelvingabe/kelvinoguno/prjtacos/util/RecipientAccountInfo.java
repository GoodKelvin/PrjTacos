package com.kelvingabe.kelvinoguno.prjtacos.util;

import java.util.List;

public class RecipientAccountInfo {

    private List<String> accName;
    private List<String> accNumber;
    private List<String> accBank;


    public List<String> getAccName() {
        return accName;
    }

    public void setAccName(List<String> accName) {
        this.accName = accName;
    }

    public List<String> getAccNumber() {
        return accNumber;
    }

    public void setAccNumber(List<String> accNumber) {
        this.accNumber = accNumber;
    }

    public List<String> getAccBank() {
        return accBank;
    }

    public void setAccBank(List<String> accBank) {
        this.accBank = accBank;
    }
}
